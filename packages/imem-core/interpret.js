function interpretIMEM(imem, score) {
  var eff = imem.alpha_net * imem.beta_net * imem.gamma_net;
  return {
    score: interpretScore(score),
    alpha: interpretAlpha(imem.alpha_net),
    beta: interpretBeta(imem.beta_net),
    gamma: interpretGamma(imem.gamma_net),
    efficiency: interpretEfficiency(eff),
  };
}

